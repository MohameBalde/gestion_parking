     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $query = \App\Models\HistoriqueVehicule::query();
        if ($request->filled('plaque')) {
            $query->where('plaque', 'like', '%' . $request->plaque . '%');
        }
        if ($request->filled('vehicule_id')) {
            $query->where('vehicule_id', $request->vehicule_id);
        }
        $historiques = $query->get();
        return view('historiques.index', compact('historiques'));
    }

